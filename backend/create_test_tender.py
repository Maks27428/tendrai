from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.enums import TA_CENTER, TA_JUSTIFY
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
from reportlab.lib.units import cm
from reportlab.lib import colors
from reportlab.pdfbase import pdfmetrics
from reportlab.pdfbase.ttfonts import TTFont

OUTPUT_DIR = os.path.join(os.path.dirname(__file__), 'seed_data', 'tenders')
os.makedirs(OUTPUT_DIR, exist_ok=True)

# Try to register a Cyrillic-capable font
try:
    pdfmetrics.registerFont(TTFont('Arial', 'C:/Windows/Fonts/arial.ttf'))
    pdfmetrics.registerFont(TTFont('Arial-Bold', 'C:/Windows/Fonts/arialbd.ttf'))
    FONT = 'Arial'
    FONT_BOLD = 'Arial-Bold'
except:
    FONT = 'Helvetica'
    FONT_BOLD = 'Helvetica-Bold'


def create_tender_pdf(filename, tender_data):
    path = os.path.join(OUTPUT_DIR, filename)
    doc = SimpleDocTemplate(path, pagesize=A4,
                            leftMargin=2*cm, rightMargin=2*cm,
                            topMargin=2*cm, bottomMargin=2*cm)

    styles = getSampleStyleSheet()
    styles.add(ParagraphStyle(name='TitleRU', fontName=FONT_BOLD, fontSize=14,
                              alignment=TA_CENTER, spaceAfter=20, leading=18))
    styles.add(ParagraphStyle(name='HeadingRU', fontName=FONT_BOLD, fontSize=11,
                              spaceAfter=10, spaceBefore=15, leading=14))
    styles.add(ParagraphStyle(name='BodyRU', fontName=FONT, fontSize=10,
                              alignment=TA_JUSTIFY, spaceAfter=8, leading=13))
    styles.add(ParagraphStyle(name='SmallRU', fontName=FONT, fontSize=9,
                              alignment=TA_JUSTIFY, spaceAfter=6, leading=11))

    story = []
    s = styles

    # Header
    story.append(Paragraph("РЕСПУБЛИКА КАЗАХСТАН", s['TitleRU']))
    story.append(Paragraph("ТЕНДЕРНАЯ ДОКУМЕНТАЦИЯ", s['TitleRU']))
    story.append(Paragraph(f"Объявление № {tender_data['number']}", s['TitleRU']))
    story.append(Spacer(1, 20))

    # General info
    story.append(Paragraph("1. ОБЩИЕ СВЕДЕНИЯ", s['HeadingRU']))
    story.append(Paragraph(f"<b>Наименование закупки:</b> {tender_data['title']}", s['BodyRU']))
    story.append(Paragraph(f"<b>Заказчик:</b> {tender_data['customer']}", s['BodyRU']))
    story.append(Paragraph(f"<b>Способ закупки:</b> {tender_data['method']}", s['BodyRU']))
    story.append(Paragraph(f"<b>Общая сумма закупки:</b> {tender_data['amount']}", s['BodyRU']))
    story.append(Paragraph(f"<b>Срок окончания приема заявок:</b> {tender_data['deadline']}", s['BodyRU']))
    story.append(Paragraph(f"<b>Место поставки:</b> {tender_data['location']}", s['BodyRU']))
    story.append(Paragraph(f"<b>Срок поставки:</b> {tender_data['delivery_period']}", s['BodyRU']))

    # Qualification requirements
    story.append(Paragraph("2. КВАЛИФИКАЦИОННЫЕ ТРЕБОВАНИЯ К ПОТЕНЦИАЛЬНОМУ ПОСТАВЩИКУ", s['HeadingRU']))
    for i, req in enumerate(tender_data['qualification_reqs'], 1):
        story.append(Paragraph(f"2.{i}. {req}", s['BodyRU']))

    # Technical specification
    story.append(Paragraph("3. ТЕХНИЧЕСКАЯ СПЕЦИФИКАЦИЯ", s['HeadingRU']))
    story.append(Paragraph(tender_data['tech_spec_intro'], s['BodyRU']))

    if tender_data.get('tech_table'):
        table_data = [['№', 'Наименование', 'Ед. изм.', 'Кол-во', 'Требования']]
        for row in tender_data['tech_table']:
            table_data.append(row)
        t = Table(table_data, colWidths=[1*cm, 5*cm, 2*cm, 2*cm, 6*cm])
        t.setStyle(TableStyle([
            ('FONTNAME', (0, 0), (-1, 0), FONT_BOLD),
            ('FONTNAME', (0, 1), (-1, -1), FONT),
            ('FONTSIZE', (0, 0), (-1, -1), 8),
            ('GRID', (0, 0), (-1, -1), 0.5, colors.black),
            ('BACKGROUND', (0, 0), (-1, 0), colors.lightgrey),
            ('VALIGN', (0, 0), (-1, -1), 'TOP'),
        ]))
        story.append(t)
        story.append(Spacer(1, 10))

    # Financial requirements
    story.append(Paragraph("4. ФИНАНСОВЫЕ ТРЕБОВАНИЯ", s['HeadingRU']))
    for i, req in enumerate(tender_data['financial_reqs'], 1):
        story.append(Paragraph(f"4.{i}. {req}", s['BodyRU']))

    # Document requirements
    story.append(Paragraph("5. ПЕРЕЧЕНЬ ДОКУМЕНТОВ, ПРЕДОСТАВЛЯЕМЫХ ПОТЕНЦИАЛЬНЫМ ПОСТАВЩИКОМ", s['HeadingRU']))
    for i, doc_req in enumerate(tender_data['doc_reqs'], 1):
        story.append(Paragraph(f"5.{i}. {doc_req}", s['BodyRU']))

    # Contract terms
    story.append(Paragraph("6. УСЛОВИЯ ДОГОВОРА", s['HeadingRU']))
    for i, term in enumerate(tender_data['contract_terms'], 1):
        story.append(Paragraph(f"6.{i}. {term}", s['BodyRU']))

    # Evaluation criteria
    story.append(Paragraph("7. КРИТЕРИИ ОЦЕНКИ И СОПОСТАВЛЕНИЯ ЗАЯВОК", s['HeadingRU']))
    for i, crit in enumerate(tender_data['eval_criteria'], 1):
        story.append(Paragraph(f"7.{i}. {crit}", s['BodyRU']))

    # Additional conditions
    if tender_data.get('additional'):
        story.append(Paragraph("8. ДОПОЛНИТЕЛЬНЫЕ УСЛОВИЯ", s['HeadingRU']))
        for i, add in enumerate(tender_data['additional'], 1):
            story.append(Paragraph(f"8.{i}. {add}", s['BodyRU']))

    doc.build(story)
    print(f"Created: {path}")
    return path


