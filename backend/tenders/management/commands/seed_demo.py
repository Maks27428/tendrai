# ---------------------------------------------------------------------------
# Вспомогательная функция создания фиктивного PDF-файла
# ---------------------------------------------------------------------------

def _make_dummy_pdf(title: str) -> ContentFile:
    """Возвращает минимальный валидный PDF-файл для поля pdf_file."""
    # Минимальный PDF 1.4, который проходит валидацию FileField без ошибок
    content = (
        b"%PDF-1.4\n"
        b"1 0 obj<</Type/Catalog/Pages 2 0 R>>endobj\n"
        b"2 0 obj<</Type/Pages/Kids[3 0 R]/Count 1>>endobj\n"
        b"3 0 obj<</Type/Page/MediaBox[0 0 612 792]/Parent 2 0 R"
        b"/Resources<<>>>>endobj\n"
        b"xref\n0 4\n"
        b"0000000000 65535 f \n"
        b"0000000009 00000 n \n"
        b"0000000058 00000 n \n"
        b"0000000115 00000 n \n"
        b"trailer<</Size 4/Root 1 0 R>>\n"
        b"startxref\n210\n%%EOF\n"
    )
    safe_name = title[:50].replace(" ", "_").replace("/", "-")
    return ContentFile(content, name=f"demo_{safe_name}.pdf")


